/* longstrg.c -- 打印较长的字符串 */

#include <stdio.h>

int main(int argc, char **argv)
{
    printf ("Here's one way to print a");
    printf ("long string.\n");
    printf ("Here's another way to print a \
long string.\n");
    printf ("Here's the newest way to print a "
              "long string.\n");

    return 0;
}
